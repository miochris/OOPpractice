package com.fdmgroup.generics;

public class Pair <T,M> {
	private T part1;
	private M part2;
	
	
	public T getPart1() {
		return part1;
	}
	public void setPart1(T part1) {
		this.part1 = part1;
	}
	public M getPart2() {
		return part2;
	}
	public void setPart2(M part2) {
		this.part2 = part2;
	}
	

}
